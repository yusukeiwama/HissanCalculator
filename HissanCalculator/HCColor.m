#import "HCColor.h"

@implementation HCColor

+ (UIColor *)blackBoardColor
{
	return [UIColor colorWithRed:60.0 / 255.0
												 green:100.0 / 255.0
													blue:80.0 / 255.0
												 alpha:1.0];
}

+ (UIColor *)highlightColor
{
	return [UIColor colorWithRed:90.0 / 255.0
												 green:150.0 / 255.0
													blue:120.0 / 255.0
												 alpha:1.0];
}

@end
